package com.java.Array;

final class MyResult {
	
    private final int sumPositives;
    private final int sumNegatives;
    
	public MyResult(int sumPositives, int sumNegatives) {
		super();
		this.sumPositives = sumPositives;
		this.sumNegatives = sumNegatives;
	}
	
	public int getSumPositive() {
		return sumPositives;
	}
	
	public int getSumNegative() {
		return sumNegatives;
	}
}    
    

  